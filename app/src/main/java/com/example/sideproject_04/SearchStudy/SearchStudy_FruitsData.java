package com.example.sideproject_04.SearchStudy;

public class SearchStudy_FruitsData {

    public String name;
    public String image;

    public SearchStudy_FruitsData(String name, String image) {
        this.name = name;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}